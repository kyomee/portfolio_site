let currentLanguage = 'en';

function getTranslation(key) {
  return translations[currentLanguage][key] || key;
}

function setLanguage(lang) {
  if (translations[lang]) {
    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);
    updatePageContent();
    updateLanguageSwitcher();
  }
}

function updatePageContent() {
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    const translation = getTranslation(key);
    
    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
      element.placeholder = translation;
    } else if (element.hasAttribute('data-i18n-html')) {
      element.innerHTML = translation;
    } else {
      element.textContent = translation;
    }
  });

  document.querySelectorAll('[data-i18n-alt]').forEach(element => {
    const key = element.getAttribute('data-i18n-alt');
    element.alt = getTranslation(key);
  });

  const titleElement = document.querySelector('title[data-i18n]');
  if (titleElement) {
    document.title = getTranslation('pageTitle');
  } else {
    document.title = getTranslation('pageTitle');
  }
  
  const htmlLang = document.documentElement.getAttribute('lang');
  if (htmlLang !== currentLanguage) {
    document.documentElement.setAttribute('lang', currentLanguage);
  }
}

function updateLanguageSwitcher() {
  const switcher = document.getElementById('language-switcher');
  const switcherMobile = document.getElementById('language-switcher-mobile');
  if (switcher) {
    switcher.textContent = currentLanguage === 'en' ? '한국어' : 'English';
  }
  if (switcherMobile) {
    switcherMobile.textContent = currentLanguage === 'en' ? '한국어' : 'English';
  }
}

function initI18n() {
  const savedLanguage = localStorage.getItem('preferredLanguage');
  if (savedLanguage && translations[savedLanguage]) {
    currentLanguage = savedLanguage;
  } else {
    currentLanguage = 'en';
  }
  
  updatePageContent();
  updateLanguageSwitcher();
}

function toggleLanguage() {
  const newLang = currentLanguage === 'en' ? 'ko' : 'en';
  setLanguage(newLang);
}

